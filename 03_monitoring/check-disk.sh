#!/bin/bash

# ==============================================
# check-disk.sh - Мониторинг свободного места на дисках
# ==============================================

# Устанавливаем порог свободного места в процентах (когда начинать бить тревогу)
THRESHOLD=10

echo "============================================="
echo "      МОНИТОРИНГ ДИСКОВОЙ ПОДСИСТЕМЫ"
echo "============================================="
echo "Порог предупреждения: менее ${THRESHOLD}% свободного места"
echo "============================================="

# Флаг: был ли найден диск, превышающий порог?
ALERT_FLAG=0

# Цикл по всем строкам вывода df -h (кроме первой строки с заголовками)
df -h | grep -v "tmpfs" | grep -v "udev" | tail -n +2 | while read line; do
    # Извлекаем нужные поля. Для df -h колонки: Файловая система, Размер, Использовано, Доступно, Использовано%, Точка монтирования
    FILESYSTEM=$(echo $line | awk '{print $1}')
    SIZE=$(echo $line | awk '{print $2}')
    USED=$(echo $line | awk '{print $3}')
    AVAIL=$(echo $line | awk '{print $4}')
    USE_PERCENT=$(echo $line | awk '{print $5}' | sed 's/%//') # Убираем знак %
    MOUNT=$(echo $line | awk '{print $6}')

    # Пропускаем строки, где поле USE_PERCENT не число (на всякий случай)
    if ! [[ "$USE_PERCENT" =~ ^[0-9]+$ ]]; then
        continue
    fi

    # Вычисляем процент свободного места
    FREE_PERCENT=$((100 - USE_PERCENT))

    # Выводим информацию о диске
    printf "%-15s %-8s %-8s %-8s %6s%% (свободно %3s%%) -> %s\n" \
           "$FILESYSTEM" "$SIZE" "$USED" "$AVAIL" "$USE_PERCENT" "$FREE_PERCENT" "$MOUNT"

    # Проверяем, не превышен ли порог
    if [ $FREE_PERCENT -lt $THRESHOLD ]; then
        echo "      ⚠️  ВНИМАНИЕ: На диске $MOUNT осталось менее ${THRESHOLD}% свободного места!"
        ALERT_FLAG=1
    fi
done

echo "============================================="

# Если был флаг тревоги, завершаем скрипт с кодом ошибки 1
if [ $ALERT_FLAG -eq 1 ]; then
    echo "🔴 Обнаружены критические диски!"
    exit 1
else
    echo "✅ Все диски в порядке."
    exit 0
fi
