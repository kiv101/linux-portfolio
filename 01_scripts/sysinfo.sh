#!/bin/bash

# ========================================
# sysinfo.sh - информация о системе
# Для портфолио Linux администратора
# ========================================

echo "======================================"
echo "         СИСТЕМНАЯ ИНФОРМАЦИЯ"
echo "======================================"

# 1. Дата и время
echo "📅 Дата и время: $(date)"

# 2. Имя хоста
echo "🖥️  Имя хоста: $(hostname)"

# 3. Кто залогинен
echo "👤 Текущий пользователь: $(whoami)"

# 4. Время работы системы (uptime)
echo "⏱️  Аптайм: $(uptime -p)"

# 5. Загрузка процессора
echo "📊 Загрузка CPU:"
uptime | awk -F'load average:' '{print $2}'

# 6. Память (оперативная)
echo "💾 Оперативная память:"
free -h | grep -E "Mem|Swap"

# 7. Диски (свободное место)
echo "💿 Свободное место на дисках:"
df -h | grep -v "tmpfs" | grep -v "udev"

# 8. Топ-5 процессов по CPU
echo "🔥 Топ-5 процессов по CPU:"
ps aux --sort=-%cpu | head -6

# 9. Топ-5 процессов по памяти
echo "🧠 Топ-5 процессов по памяти:"
ps aux --sort=-%mem | head -6

# 10. Количество ошибок в системном логе (за последние 10 строк)
echo "⚠️  Ошибки в системном логе (последние 10 строк):"
journalctl -p err -n 10 2>/dev/null || echo "Нет доступа к journalctl"

echo "======================================"
echo "         ОТЧЁТ СФОРМИРОВАН"
echo "======================================"
